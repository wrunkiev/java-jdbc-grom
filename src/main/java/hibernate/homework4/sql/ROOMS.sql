CREATE TABLE ROOMS(
ID NUMBER NOT NULL ,
ROOM_NUMBER_OF_GUESTS NUMBER,
ROOM_PRICE DECIMAL(10,2),
ROOM_BREAKFAST_INCLUDED CHAR DEFAULT 'N' NOT NULL,
ROOM_PETS_ALLOWED CHAR DEFAULT 'N' NOT NULL,
ROOM_DATE_AVAILABLE_FROM TIMESTAMP,
ROOM_HOTEL_ID NUMBER,
CONSTRAINT ROOM_ID PRIMARY KEY(ID),
CONSTRAINT ROOM_HOTEL_FK FOREIGN KEY(ROOM_HOTEL_ID) REFERENCES HOTELS(ID)
);






















